#Escape Charaters
# \n ---> New Line
# \t ---> Horizontal Tab
# \r ---> Carriage Return
# \b ---> Back Space
# \f ---> Form Feed
# \v ---> Vertical Tab
# \' ---> Singel Quote
# \" ---> Double Quote
# \\ ---> Back Slash

print("Hello,\n I'm from India")
print("Hello,\t I'm from India")
print("Hello,\r I'm from India")
print("Hello,\b I'm from India")
print("Hello,\f I'm from India")
print("Hello,\v I'm from India")
print("Hello,\' I'm from India\'")
print("Hello,\" I'm from India\"")
print("Hello,\\ I'm from India\\")